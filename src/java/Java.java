
public class Java {
    public static int doubleInteger(int i) {
        
        return i * 2;
        
    }
    public static void main(String[] args) {
        int input = 5;
        int output = doubleInteger(input);
        System.out.println("Bilangan awal :" + input);
        System.out.println("Bilangan setelah digandakan " + output);
    }
    
}
